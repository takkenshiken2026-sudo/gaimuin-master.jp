def _category_label(category: str) -> str:
    if category == "金融商品・サービス":
        return "金融商品"
    if category == "勧誘・販売規則":
        return "勧誘・販売"
    if category == "金融商品取引法":
        return "金商法"
    return "法令・業務"


def _article_title(term: str, importance: str) -> str:
    suffix = {
        "A": "証券外務員試験で頻出の要点",
        "B": "証券外務員試験の意味と論点",
        "C": "証券外務員試験の基礎用語",
        "S": "証券外務員試験の重要用語",
    }.get((importance or "B").strip().upper(), "証券外務員試験の要点")
    title = f"{term}とは｜{suffix}"
    if len(title) > 54:
        title = f"{term}とは｜証券外務員試験の要点"
    if len(title) > 54:
        title = f"{term}｜証券外務員試験"[:54]
    return title


def build_short_def(term: str, gist: str) -> str:
    return f"{term}は、{gist}。外務員試験では定義の言い換えと適用場面の判断が頻出です。"


def build_definition(term: str, gist: str, legal: str, category: str) -> str:
    legal_part = f"根拠は主に{legal}です。" if legal else ""
    return (
        f"{term}は{_category_label(category)}分野の重要語で、{gist}。"
        f"{legal_part}"
        f"試験では条文・協会規則の表現と日常語の違いに注意して整理します。"
    )


def build_explanation(term: str, focus: str, category: str) -> str:
    return (
        f"証券外務員試験では、{term}が出る設問で{focus}が問われます。"
        f"{_category_label(category)}の問題では、主体（誰が）・要件（いつ・何を）・例外の有無を"
        f"選択肢ごとに照合するのが得点の近道です。外務員必携の該当章と過去問の正誤パターンをセットで確認してください。"
    )


def build_article_lead(term: str, gist: str) -> str:
    return (
        f"{term}は{EXAM_NAME}で繰り返し問われる用語です。"
        f"{gist}。"
        f"本記事では意味・試験での論点・混同しやすい点を、受験者が実務判断の順序で整理できるよう解説します。"
    )


def build_term_detail_body(term: str, gist: str, legal: str, focus: str, category: str) -> str:
    p1 = (
        f"{term}について、まず{gist}。"
        f"外務員の現場では、顧客への説明・社内手続・記録保存のどこに関わるかを意識すると定着しやすいです。"
    )
    if legal:
        p1 += f" 法令・規則上の位置づけは{legal}に関係し、数値や期限が付く場合は公式テキストの最新版で確認します。"
    p2 = (
        f"試験対策では、{focus}を軸に類似語との差を表にまとめるとよいでしょう。"
        f"{_category_label(category)}分野の設問は「正しいものはどれか」「誤っているものはどれか」の両方で出題され、"
        f"一見正しそうな部分引用が誤答になっているパターンが多いです。"
    )
    return f"{p1}\n\n{p2}"


def build_exam_points(term: str, focus: str, category: str) -> str:
    return (
        f"{focus}の要件を条文・規則で確認する;"
        f"{term}と{_category_label(category)}の近縁語の違いを表で整理する"
    )


def build_common_mistakes(term: str, focus: str) -> str:
    return (
        f"{term}では、{focus}の一部だけを覚えて全体像を取り違える誤りが多いです。"
        f"また、類似語の定義をそのまま当てはめると誤答になりやすいので、主体と効果をセットで確認してください。"
    )


def build_memory_tip(term: str, category: str, legal: str) -> str:
    hint = legal.split("第")[0] if legal and "第" in legal else _category_label(category)
    return f"{term}は「{hint}」のキーワードと結び付け、関連する2語と三点セットで覚えると再現しやすいです。"


def build_example_question(term: str, focus: str) -> str:
    return f"「{term}に関する次の記述のうち、{EXAM_NAME}の出題範囲として誤っているものはどれか。」"


def build_example_answer(term: str, focus: str) -> str:
    return f"×。{focus}の例外や主体を取り違えた記述が誤答の典型です。{term}の定義と要件を条文ベースで確認してください。"



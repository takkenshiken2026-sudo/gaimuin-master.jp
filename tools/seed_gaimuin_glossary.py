def build_row(spec: tuple[str, str, str, str, str, str, str]) -> dict[str, str]:
    term, category, importance, tags, gist, legal, focus = spec
    faq = build_faq(term, gist, focus, legal)
    row = {
        "term": term,
        "category": category,
        "tags": tags.replace(",", ";"),
        "short_def": build_short_def(term, gist),
        "definition": build_definition(term, gist, legal, category),
        "related_terms": "",
        "legal_basis": legal,
        "importance": importance,
        "explanation": build_explanation(term, focus, category),
        "article_title": _article_title(term, importance),
        "article_lead": build_article_lead(term, gist),
        "term_detail_body": build_term_detail_body(term, gist, legal, focus, category),
        "exam_points": build_exam_points(term, focus, category),
        "common_mistakes": build_common_mistakes(term, focus),
        "memory_tip": build_memory_tip(term, category, legal),
        "example_question": build_example_question(term, focus),
        "example_answer": build_example_answer(term, focus),
        **faq,
        "diagram_id": "",
        "slug": "",
        "related_links": "",
    }
    return row


def assign_related_terms(rows: list[dict[str, str]]) -> None:
    by_cat: dict[str, list[int]] = defaultdict(list)
    for i, row in enumerate(rows):
        by_cat[row["category"]].append(i)
    term_set = {r["term"] for r in rows}
    cats = list(by_cat.keys())

    def pick_related(idx: int, term: str, category: str) -> list[str]:
        chosen: list[str] = []
        for j in by_cat[category]:
            if j == idx:
                continue
            other = rows[j]["term"]
            if other not in chosen:
                chosen.append(other)
            if len(chosen) >= 3:
                break
        if len(chosen) < 2:
            for cat in cats:
                if cat == category:
                    continue
                for j in by_cat[cat]:
                    other = rows[j]["term"]
                    if other != term and other not in chosen:
                        chosen.append(other)
                    if len(chosen) >= 2:
                        break
                if len(chosen) >= 2:
                    break
        return chosen[:3]

    for i, row in enumerate(rows):
        rel = pick_related(i, row["term"], row["category"])
        if len(rel) < 2:
            raise RuntimeError(f"related_terms を割り当てられません: {row['term']}")
        row["related_terms"] = ";".join(rel)


def load_fieldnames(csv_path: Path) -> list[str]:
    if csv_path.is_file():
        with csv_path.open(encoding="utf-8-sig", newline="") as f:
            reader = csv.DictReader(f)
            if reader.fieldnames:
                return list(reader.fieldnames)
    base = sorted(GLOSSARY_BASE_REQUIRED)
    return base + ["diagram_id", "slug", "related_links"]


def validate_rows(rows: list[dict[str, str]]) -> list[str]:
    lookup: dict[str, str] = {}
    for row in rows:
        term = row["term"]
        lookup[term] = f"terms/{term}.html"
        lookup[lookup_key(term)] = lookup[term]
    errors: list[str] = []
    for i, row in enumerate(rows, start=2):
        for issue in check_glossary_row(row, term_lookup=lookup, line=i):
            if issue.level == "ERROR":
                errors.append(f"line {i} ({row.get('term')}): {issue.message}")
    return errors


def write_csv(path: Path, fieldnames: list[str], rows: list[dict[str, str]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames, lineterminator="\n", extrasaction="ignore")
        writer.writeheader()
        for row in rows:
            writer.writerow({k: row.get(k, "") for k in fieldnames})


def main() -> int:
    parser = argparse.ArgumentParser(description="証券外務員向け glossary_terms.csv を300件で生成します。")
    parser.add_argument(
        "--root",
        type=Path,
        default=REPO_ROOT,
        help="サイトルート（既定: スクリプトの親ディレクトリ）",
    )
    parser.add_argument("--dry-run", action="store_true", help="CSVを書き込まず検証のみ")
    args = parser.parse_args()

    site_root = args.root.resolve()
    if not (site_root / "site-config.json").is_file():
        print(f"error: site-config.json が見つかりません: {site_root}", file=sys.stderr)
        return 1

    os.environ["EXAM_SITE_ROOT"] = str(site_root)

    csv_path = site_root / "data" / "glossary_terms.csv"
    fieldnames = load_fieldnames(csv_path)

    rows = [build_row(spec) for spec in TERMS]
    assign_related_terms(rows)

    errors = validate_rows(rows)
    if errors:
        print("validation errors:", file=sys.stderr)
        for e in errors[:20]:
            print(f"  {e}", file=sys.stderr)
        if len(errors) > 20:
            print(f"  ... and {len(errors) - 20} more", file=sys.stderr)
        return 1

    if args.dry_run:
        print(f"dry-run ok: {len(rows)} terms -> {csv_path}")
        print("sample:", ", ".join(r["term"] for r in rows[:3]))
        return 0

    write_csv(csv_path, fieldnames, rows)
    print(f"wrote {len(rows)} terms -> {csv_path}")
    print("sample:", ", ".join(r["term"] for r in rows[:3]))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

